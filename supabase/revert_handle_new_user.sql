-- Revert handle_new_user to the standard safe implementation
-- This removes the complex logic and just inserts the ID and basic metadata
-- to ensure logins/signups work again.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, full_name, avatar_url)
  values (
    new.id,
    new.raw_user_meta_data->>'full_name',
    new.raw_user_meta_data->>'avatar_url'
  );
  return new;
end;
$$;
